/*
 * led.c — LED 驱动 (已移除功能)
 * ══════════════════════════════════════════════════════════════
 *
 * 【移除原因】
 *   原始 ALIENTEK 开发板上 LED 接在 PA8 引脚
 *   本项目将 PA8 复用为 TIM1_CH1 PWM 输出引脚
 *   GPIO 和 PWM 不能同时使用同一引脚, 因此 LED 功能被移除
 *
 * 【PA8 引脚冲突说明】
 *   原用途: PA8 → LED (推挽输出, 高电平亮/低电平灭)
 *   现用途: PA8 → TIM1_CH1 PWM 输出 (复用推挽输出)
 *   参见 timer.c 中的 TIM1_PWM_Init() 函数
 *
 * 【保留文件原因】
 *   LED_Init() 函数保留为空实现, 原因:
 *   1. main.c 或其他模块可能仍调用 LED_Init()
 *   2. led.h 中的宏定义 (LED0/LED1) 可能被其他文件引用
 *   3. 保持工程结构完整, 避免编译链接错误
 */
#include "led.h"

/* ── LED 初始化 (空实现) ──
 * PA8 已被 TIM1 PWM 占用, 无法再用作 LED 控制
 * 此函数保留为空, 仅满足调用方的符号需求
 */
void LED_Init(void) {
    // 空 — PA8 已分配给 TIM1 CH1 PWM 输出
}
