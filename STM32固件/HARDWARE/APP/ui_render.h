/**
 * ============================================================================
 *  ui_render.h — OLED 128x64 界面渲染模块
 * ============================================================================
 *
 *  【模块概述】
 *   本模块负责将系统状态信息渲染到 0.96寸 OLED 显示屏上.
 *   使用 ALIENTEK 提供的软件I2C OLED 驱动 API, 分辨率 128x64 像素.
 *   字体大小: 8x6 像素 (每行最多21个字符, 共8行).
 *
 *  【6 种渲染模式 (对应 6+1 种应用模式, GAME 模式另见 render_game_mode)】
 *
 *   1. PWM_FG 模式 (双通道 + 频率计):
 *   ┌─────────────────────┐
 *   │ CH1: 1000Hz   50%  │  ← CH1 频率和占空比
 *   │ CH2:  500Hz   30%  │  ← CH2 频率和占空比
 *   │ RPM:  12345        │  ← 当前转速
 *   │ DIV:  1    PPR: 1  │  ← 分频系数和每转脉冲数
 *   └─────────────────────┘
 *
 *   2. FG 模式 (纯频率计):
 *   ┌─────────────────────┐
 *   │    ╔══════════╗     │
 *   │    ║  12345   ║     │  ← 大号数字显示 RPM
 *   │    ║  RPM     ║     │
 *   │    ╚══════════╝     │
 *   │ DIV: 1    PPR: 1   │  ← 分频/脉冲参数
 *   └─────────────────────┘
 *
 *   3. CH1 模式 (单通道设置):
 *   ┌─────────────────────┐
 *   │ -- CH1 设置 --      │
 *   │ 频率: 1000 Hz  [■]  │  ← 带光标的频率设置
 *   │ 占空比: 50 %   [ ]  │  ← 带光标的占空比设置
 *   │ 使能: ON        [ ]  │  ← 使能开关
 *   │ RPM: 12345          │  ← 实时转速反馈
 *   └─────────────────────┘
 *
 *   4. CH2 模式 (单通道设置):
 *      同 CH1 模式, 显示 CH2 相关参数
 *
 *   5. TEST 模式 (自动测试):
 *   ┌─────────────────────┐
 *   │ -- 自动测试 --      │
 *   │ 配置状态:           │  ← 或 "运行中..."
 *   │ 通道: CH1           │  ← 测试通道
 *   │ 频率: 1000 Hz       │  ← 目标频率
 *   │ 占空比: 50 %        │  ← 目标占空比
 *   │ 周期: 10 / 100      │  ← 当前/总周期
 *   └─────────────────────┘
 *
 *  【光标闪烁机制】
 *   blink 参数每 400ms 翻转一次 (由主循环定时器控制)
 *   blink=1 时显示光标标记 (如 [■]), blink=0 时隐藏
 *   实现编辑项的视觉闪烁提示效果
 *
 *  【渲染时机】
 *   UI_Render() 每 50ms 调用一次 (20fps 刷新率)
 *   OLED I2C 通信耗时约 2~3ms, 不会对主循环造成瓶颈
 *
 * ============================================================================
 */
#ifndef UI_RENDER_H
#define UI_RENDER_H

#include "sys.h"
#include "menu_defs.h"

// OLED 主渲染函数
// 功能: 根据当前菜单模式 (g_menu.mode) 选择对应的渲染函数,
//       将系统参数、频率/RPM数据、菜单状态绘制到 OLED 屏幕上
// 参数:
//   p     — 指向 g_params 的指针, 包含频率/占空比/使能/分频/PPR等参数
//   rpm   — 当前测量的 RPM 值 (来自 FG_CalculateRPM)
//   blink — 光标闪烁标志 (0或1), 每400ms翻转一次
//           blink=1 时当前编辑项显示高亮/闪烁标记
//           blink=0 时隐藏标记, 产生闪烁效果
// 调用时机: 主循环中每 50ms 调用一次
// 注意: 此函数会调用 OLED 清屏, 因此每次都是全屏重绘 (非局部刷新)
void UI_Render(SystemParams *p, u16 rpm, u8 blink);

#endif
