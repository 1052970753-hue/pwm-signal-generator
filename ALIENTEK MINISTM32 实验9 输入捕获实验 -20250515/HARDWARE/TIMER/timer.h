/**
 * ============================================================================
 *  timer.h — 定时器底层驱动接口
 * ============================================================================
 *
 *  【模块概述】
 *   本文件声明了系统中三个定时器的初始化函数.
 *   定时器的具体配置 (预分频、自动重装载、中断等) 在 timer.c 中实现.
 *
 *  【定时器资源分配】
 *
 *   ┌────────┬─────────────┬──────────┬────────────────────────────────────┐
 *   │ 定时器 │ 通道/引脚    │ 用途     │ 详细说明                           │
 *   ├────────┼─────────────┼──────────┼────────────────────────────────────┤
 *   │ TIM1   │ CH1 (PA8)   │ PWM 输出 │ 通道1 PWM 信号输出                 │
 *   │        │             │ (CH1)    │ 高级定时器, 支持互补/死区,         │
 *   │        │             │          │ 本项目仅使用基本 PWM 模式          │
 *   │        │             │          │ ARR/PSC 由 pwm_engine 自适应计算   │
 *   ├────────┼─────────────┼──────────┼────────────────────────────────────┤
 *   │ TIM4   │ CH1 (PB6)   │ PWM 输出 │ 通道2 PWM 信号输出                 │
 *   │        │             │ (CH2)    │ 通用定时器, 16位计数器             │
 *   │        │             │          │ ARR/PSC 由 pwm_engine 自适应计算   │
 *   ├────────┼─────────────┼──────────┼────────────────────────────────────┤
 *   │ TIM2   │ CH1 (PA0)   │ 输入捕获 │ 频率计/转速表信号输入              │
 *   │        │             │ (FG)     │ 测量高电平脉宽 (上升沿→下降沿)     │
 *   │        │             │          │ PSC=71, 时基 1MHz (1us 分辨率)    │
 *   │        │             │          │ 开启捕获中断和更新中断(溢出计数)   │
 *   └────────┴─────────────┴──────────┴────────────────────────────────────┘
 *
 *   注意: TIM3 被用于编码器接口模式, 其初始化在 Encoder.c 中完成, 不在此文件.
 *
 *  【PWM 输出模式说明 (TIM1/TIM4)】
 *   模式: PWM 模式1 (CNT < CCR 时输出有效电平)
 *   极性: 高电平有效 (OCxP=0)
 *   预装载: 使能 (写入 CCR 后在下一个更新事件生效, 避免毛刺)
 *
 *  【输入捕获模式说明 (TIM2)】
 *   模式: 边沿检测触发捕获
 *   初始触发边沿: 上升沿 (检测到后切换为下降沿, 如此交替)
 *   中断:
 *     - 捕获中断 (CC1IE): 捕获到边沿时触发, 读取 CCR1 值
 *     - 更新中断 (UIE): 计数器溢出时触发, 累加溢出次数
 *   脉宽计算: (溢出次数 × 65536 + CCR差值) = 高电平脉宽(us)
 *
 *  【参数 arr 和 psc 的含义】
 *   arr — 自动重装载值 (Auto-Reload Register)
 *         定时器计数到此值后清零重新计数
 *         PWM 频率 = Fclk / ((arr+1) × (psc+1))
 *
 *   psc — 预分频值 (Prescaler)
 *         对定时器时钟进行分频
 *         实际计数频率 = Fclk / (psc+1)
 *
 * ============================================================================
 */
#ifndef __TIMER_H
#define __TIMER_H

#include "sys.h"

// TIM1 PWM 初始化 (通道1: PA8)
// 参数:
//   arr — 自动重装载值 (决定 PWM 周期: T = (arr+1) / (Fclk/(psc+1)))
//   psc — 预分频值 (决定计数频率: Fcnt = 72MHz / (psc+1))
// 功能: 配置 TIM1 CH1 为 PWM 输出模式, 使能预装载, 但不使能输出
//       (使能/禁用由 pwm_engine 的 PWM_EnableChannel 控制)
// 调用时机: 由 PWM_Init() 内部调用, 不需要用户直接调用
void TIM1_PWM_Init(u16 arr, u16 psc);

// TIM4 PWM 初始化 (通道1: PB6)
// 参数: 同 TIM1_PWM_Init
// 功能: 配置 TIM4 CH1 为 PWM 输出模式
// 调用时机: 由 PWM_Init() 内部调用
void TIM4_PWM_Init(u16 arr, u16 psc);

// TIM2 输入捕获初始化 (通道1: PA0)
// 参数:
//   arr — 自动重装载值 (建议 65535, 最大测量范围)
//   psc — 预分频值 (建议 71, 即 72MHz/72=1MHz 时基, 1us 分辨率)
// 功能: 配置 TIM2 CH1 为输入捕获模式, 使能捕获中断和更新中断
//       初始触发边沿为上升沿
// 调用时机: 由 FG_Init() 内部调用
void TIM2_Cap_Init(u16 arr, u16 psc);

// 捕获到的高电平脉宽 (微秒), 由 TIM2 捕获中断更新
// 值含义:
//   > 0 — 有效脉宽, 可用于计算频率: freq = 1000000 / (fg_period_us × 2)
//   = 0 — 无信号输入或频率过低 (超过测量范围)
// 注意: volatile 修饰, 因为在中断中修改、主循环中读取
extern volatile u32 fg_period_us;

#endif
