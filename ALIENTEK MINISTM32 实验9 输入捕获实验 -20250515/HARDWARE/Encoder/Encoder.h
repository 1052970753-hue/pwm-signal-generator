/**
 * ============================================================================
 *  Encoder.h — 旋转编码器驱动接口
 * ============================================================================
 *
 *  【模块概述】
 *   本模块驱动一个机械旋转编码器(带按键), 用于菜单导航和参数调节.
 *   编码器通过 TIM3 硬件正交解码计数, 按键通过 GPIO 轮询检测.
 *
 *  【硬件连接】
 *   编码器 A 相 → PA6 (TIM3_CH1) — 正交信号 A
 *   编码器 B 相 → PA7 (TIM3_CH2) — 正交信号 B
 *   编码器按键  → PB8 (GPIO)     — 按下接地, 内部上拉
 *
 *  【正交解码原理】
 *   旋转编码器输出两路相位差90度的方波信号:
 *
 *   顺时针旋转 (CW):           逆时针旋转 (CCW):
 *   A: ─┐  ┌─  ┌─             A: ─┐  ┌─  ┌─
 *      ─┘  └─  └─                ─┘  └─  └─
 *   B: ──┐  ┌──  ┌             B: ─┐  ┌─  ┌─
 *      ──┘  └──  └                ─┘  └─  └─
 *   A超前B 90°                  B超前A 90°
 *
 *   TIM3 配置为编码器模式3 (TI1FP1 + TI2FP2 双边沿计数):
 *   - CW 旋转: 计数器递增
 *   - CCW 旋转: 计数器递减
 *   - 每个脉冲周期计数4次 (4倍频), 分辨率高
 *
 *  【事件检测机制】
 *   Encoder_Poll() 每帧调用, 检测以下事件:
 *
 *   1. 旋转事件 (CW/CCW):
 *      读取 TIM3 计数器当前值, 与上一次读取的值比较:
 *      - 差值 > 阈值  → 产生 INPUT_CW (顺时针) 事件
 *      - 差值 < -阈值 → 产生 INPUT_CCW (逆时针) 事件
 *      - 差值在阈值内 → 无事件 (消除抖动)
 *      每次读取后更新"上一次"记录, 累积值归零
 *
 *   2. 按键事件 (ENTER):
 *      轮询 PB8 电平, 实现软件消抖:
 *      - 检测到下降沿 (释放到按下) → 记录按下时间
 *      - 持续低电平超过消抖时间(约20ms) → 确认按下
 *      - 检测到上升沿 (按下到释放) → 产生 INPUT_ENTER 事件
 *
 *  【事件类型 (InputEvent_t / InputEvent)】
 *   INPUT_NONE  — 无事件 (默认返回值)
 *   INPUT_CW    — 顺时针旋转 (数值增大/光标下移)
 *   INPUT_CCW   — 逆时针旋转 (数值减小/光标上移)
 *   INPUT_ENTER — 按键按下确认 (进入编辑/确认/切换)
 *
 *  【使用流程】
 *   1. 系统启动调用 Encoder_Init() 初始化 TIM3 编码器模式和按键 GPIO
 *   2. 每 1~5ms 调用 Encoder_Poll() 检测事件
 *   3. 如果返回值 != INPUT_NONE, 传递给 Menu_Process() 处理
 *
 * ============================================================================
 */
#ifndef __ENCODER_H
#define __ENCODER_H

#include "sys.h"

// 输入事件类型枚举 (u8 类型别名)
// 取值定义见 menu_defs.h 或实现文件中的宏/枚举:
//   INPUT_NONE  = 0  (无事件)
//   INPUT_CW    = 1  (顺时针旋转)
//   INPUT_CCW   = 2  (逆时针旋转)
//   INPUT_ENTER = 3  (按键确认)
typedef u8 InputEvent_t;

// 编码器初始化
// 功能:
//   - 配置 TIM3 为编码器模式3 (双通道双边沿计数)
//   - 配置 PA6/PA7 为浮空输入 (编码器A/B相)
//   - 配置 PB8 为内部上拉输入 (编码器按键)
//   - 初始化软件消抖状态变量
// 调用时机: 系统启动时, 在定时器和GPIO时钟使能之后
void Encoder_Init(void);

// 轮询编码器状态, 返回检测到的事件
// 返回值: InputEvent_t 类型的事件
//   - INPUT_NONE  — 无事件 (未旋转或未按下)
//   - INPUT_CW    — 检测到顺时针旋转
//   - INPUT_CCW   — 检测到逆时针旋转
//   - INPUT_ENTER — 检测到按键按下释放
// 调用时机: 主循环中频繁调用 (建议每 1~5ms)
// 注意: 此函数是非阻塞的, 使用软件消抖避免误触发
InputEvent_t Encoder_Poll(void);

// 设置编码器时间基准 (可选, 用于消抖计时)
// 参数 tick — 当前系统滴答值 (ms)
// 功能: 提供时间参考给按键消抖逻辑使用
//       如果系统有 SysTick 中断自动递增计数器, 此函数可能不需要手动调用
void Encoder_SetTick(u32 tick);

#endif
