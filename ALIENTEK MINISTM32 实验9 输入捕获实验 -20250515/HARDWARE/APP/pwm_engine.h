/**
 * ============================================================================
 *  pwm_engine.h — 双通道 PWM 频率/占空比控制引擎
 * ============================================================================
 *
 *  【模块概述】
 *   本模块封装了 TIM1 和 TIM4 的 PWM 输出功能, 提供高级接口用于
 *   设置频率、占空比、使能/禁用输出. 支持 1Hz ~ 100kHz 的宽频率范围.
 *
 *  【硬件映射】
 *   CH1 → TIM1 CH1 (PA8) — 高级定时器, 支持互补输出/死区, 本项目仅用基本PWM
 *   CH2 → TIM4 CH1 (PB6) — 通用定时器, 4通道, 本项目仅用CH1
 *
 *  【自适应预分频算法说明】
 *   STM32 定时器的 PWM 频率计算公式:
 *
 *       Fpwm = Fclk / ((ARR + 1) × (PSC + 1))
 *
 *   其中:
 *     Fclk  = 72MHz (系统时钟)
 *     ARR   = 自动重装载值 (16位, 范围 0~65535)
 *     PSC   = 预分频值 (16位, 范围 0~65535)
 *
 *   固定 PSC 会导致频率范围受限. 自适应算法的核心思想:
 *     1. 优先选择 PSC 使 ARR 落在 [100, 65535] 范围内
 *        - ARR 太小 (<100): 占空比分辨率太低, 精度不足
 *        - ARR 太大 (>65535): 超出硬件限制
 *     2. 计算公式: PSC = Fclk / (Ftarget × (ARR_desired + 1)) - 1
 *        - 先预设一个理想的 ARR 值 (如 1000), 计算对应 PSC
 *        - 如果 PSC > 65535, 降低 ARR 目标值重新计算
 *        - 如果 PSC < 0, 提高 ARR 目标值或限制最低频率
 *
 *   示例 (目标频率 1kHz):
 *     ARR = 999, PSC = 72000000 / (1000 × 1000) - 1 = 71
 *     实际频率 = 72000000 / (1000 × 72) = 1000Hz ✓
 *
 *   示例 (目标频率 50kHz):
 *     ARR = 1439, PSC = 72000000 / (50000 × 1440) - 1 = 0
 *     实际频率 = 72000000 / (1440 × 1) = 50000Hz ✓
 *
 *   占空比计算: CCR = (ARR + 1) × duty_pct / 100
 *     - duty_pct 范围: 0~100 (%)
 *     - CCR = 0 时输出恒低, CCR = ARR+1 时输出恒高
 *
 *  【接口使用流程】
 *   1. 系统启动时调用 PWM_Init() 初始化两个通道为默认值 (1kHz, 50%)
 *   2. 需要修改参数时调用 PWM_SetChannel() 设置频率和占空比
 *   3. 需要开关输出时调用 PWM_EnableChannel()
 *   4. 菜单修改参数后调用 PWM_UpdateFromParams() 一次性同步所有通道
 *
 * ============================================================================
 */
#ifndef PWM_ENGINE_H
#define PWM_ENGINE_H

#include "sys.h"
#include "menu_defs.h"

// PWM 引擎初始化
// 功能: 初始化 TIM1 和 TIM4 为 PWM 输出模式, 默认 1kHz / 50% 占空比
// 调用时机: 系统启动时, 在 Menu_Init() 之前调用
void PWM_Init(void);

// 设置单个通道的频率和占空比
// 参数:
//   channel  — 通道号 (0=CH1/TIM1, 1=CH2/TIM4)
//   freq_hz  — 目标频率 (Hz), 范围 1~100000
//   duty_pct — 占空比 (0~100, 单位: %)
// 注意: 此函数仅更新定时器参数, 不改变使能状态
//       如果通道处于禁用状态, 参数会在下次使能时生效
void PWM_SetChannel(u8 channel, u32 freq_hz, u8 duty_pct);

// 使能或禁用指定通道的 PWM 输出
// 参数:
//   channel — 通道号 (0=CH1, 1=CH2)
//   enable  — 1=使能输出, 0=禁用输出 (引脚保持低电平)
// 注意: 禁用时不修改定时器计数, 使能时恢复之前的波形
void PWM_EnableChannel(u8 channel, u8 enable);

// 从全局参数结构体同步更新所有通道
// 参数: params — 指向 g_params 的指针
// 功能: 读取 g_params 中的频率/占空比/使能位, 调用 SetChannel 和 EnableChannel
// 调用时机: Menu_Process() 修改参数后, 由主循环调用
void PWM_UpdateFromParams(SystemParams *params);

#endif
