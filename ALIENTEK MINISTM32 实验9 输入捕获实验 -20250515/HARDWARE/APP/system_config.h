/**
 * ============================================================================
 *  system_config.h — 系统全局配置头文件
 * ============================================================================
 *
 *  【硬件平台】
 *   MCU: STM32F103C8T6 (LQFP48, 72MHz, 64KB Flash, 20KB SRAM)
 *   开发板: ALIENTEK MiniSTM32
 *   OLED: 0.96寸 128x64 软件I2C (SSD1306 驱动)
 *
 *  【引脚分配总表】
 *  ┌──────────────┬────────┬────────────┬──────────┬────────────────────┐
 *  │  功能        │  引脚  │  定时器/外设│  引脚模式│  说明              │
 *  ├──────────────┼────────┼────────────┼──────────┼────────────────────┤
 *  │  PWM CH1     │  PA8   │  TIM1_CH1  │  AF_PP   │  通道1 PWM 输出    │
 *  │  PWM CH2     │  PB6   │  TIM4_CH1  │  AF_PP   │  通道2 PWM 输出    │
 *  │  FG 输入捕获  │  PA0   │  TIM2_CH1  │  Input   │  频率计/转速表输入  │
 *  │  编码器 A相   │  PA6   │  TIM3_CH1  │  Input   │  旋转编码器 A 相   │
 *  │  编码器 B相   │  PA7   │  TIM3_CH2  │  Input   │  旋转编码器 B 相   │
 *  │  OLED SCL    │  PA1   │  软件I2C   │  Output  │  I2C 时钟线        │
 *  │  OLED SDA    │  PA2   │  软件I2C   │  OD      │  I2C 数据线(开漏)  │
 *  │  OK 按键      │  PB8   │  GPIO      │  IPU     │  确认键(内部上拉)  │
 *  │  USART TX    │  PA9   │  USART1    │  AF_PP   │  UART 发送         │
 *  │  USART RX    │  PA10  │  USART1    │  Input   │  UART 接收         │
 *  └──────────────┴────────┴────────────┴──────────┴────────────────────┘
 *
 *  【已取消的外设】
 *   - LED0: 原占 PA8, 已让给 TIM1_CH1 PWM 输出
 *   - LED1: 原占 PB8, 已让给 OK 按键
 *   - ADC:  未使用, 节省引脚
 *   - LCD:  改用 OLED, 节省大量 IO
 *
 *  【硬件资源使用概要】
 *   TIM1 — CH1 PWM 输出 (高级定时器, 支持互补输出, 死区等, 本项目仅用基本PWM)
 *   TIM4 — CH1 PWM 输出 (通用定时器, PB6)
 *   TIM2 — CH1 输入捕获 (高电平脉宽测量, 1MHz 时基)
 *   TIM3 — 编码器接口模式 (正交解码, 硬件计数)
 *   USART1 — 串口通信 (115200bps, 帧协议, 状态上报/命令下发/CSV导出)
 *   软件I2C — OLED 显示屏驱动
 *
 *  【配置常量说明】
 *   CDC_BAUDRATE  — 串口波特率, PC端和MCU端必须一致
 *   RX_RING_SIZE  — UART接收环形缓冲区大小, 必须是2的幂次(用位与代替取模)
 *   FRAME_DATA_MAX — 协议帧中DATA字段的最大字节数
 *   TEST_BUF_SIZE  — 自动测试模式的记录缓冲区最大条数
 *
 * ============================================================================
 */
#ifndef SYSTEM_CONFIG_H
#define SYSTEM_CONFIG_H

/* ── 引脚分配表 (见文件头注释) ────────────────────────────────────────── */

// USART 串口波特率 (bps)
// 115200 是调试和通信的常用速率, 约 11.5KB/s, 足够状态上报和CSV导出
#define CDC_BAUDRATE        115200

// UART 接收环形缓冲区大小 (必须为 2 的幂次)
// 使用 RX_BUF_SIZE - 1 做位与运算代替取模, 提升中断中缓冲区索引更新效率
// 256 字节可容纳约 10 个完整状态帧, 避免高频率上报时数据丢失
#define RX_RING_SIZE        256

// 协议帧中数据域(DATA)的最大长度 (字节)
// 帧格式: [HEADER 1B][CMD 1B][LEN 1B][DATA NB][CRC8 1B]
// 32 字节足以容纳测试配置/状态数据等最大载荷
#define FRAME_DATA_MAX      32

// 自动测试记录缓冲区大小 (环形缓冲区)
// 每条记录约 20 字节, 200 条 = 4KB, 占用合理
// 超出时自动覆盖最早的记录
#define TEST_BUF_SIZE       200

#endif
