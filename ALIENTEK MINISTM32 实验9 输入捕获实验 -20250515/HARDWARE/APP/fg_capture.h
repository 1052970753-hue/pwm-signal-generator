/**
 * ============================================================================
 *  fg_capture.h — 频率计 / RPM 转速计算模块
 * ============================================================================
 *
 *  【模块概述】
 *   本模块通过 TIM2 输入捕获功能测量外部信号的频率, 并可计算电机转速 (RPM).
 *   适用于光电/霍尔传感器输出的脉冲信号, 实现非接触式转速测量.
 *
 *  【硬件配置】
 *   定时器: TIM2 通道1 (PA0)
 *   捕获模式: 上升沿触发, 测量高电平脉宽
 *   时基频率: 1MHz (PSC=71, Fclk=72MHz → 72MHz/72=1MHz)
 *   分辨率: 1us (微秒级精度)
 *   最大可测周期: 65535us (ARR=65535), 即最低频率约 15.26Hz
 *
 *  【测量原理 — 高电平脉宽法】
 *   假设外部信号是 50% 占空比的方波 (大多数传感器满足此条件):
 *
 *       ┌──────┐      ┌──────┐      ┌──────┐
 *       │      │      │      │      │      │
 *   ────┘      └──────┘      └──────┘      └───
 *   |<-- T -->|
 *
 *   1. 捕获上升沿时刻 T1 (CCR 寄存器值)
 *   2. 切换为下降沿触发
 *   3. 捕获下降沿时刻 T2
 *   4. 高电平脉宽 = T2 - T1 (单位: us)
 *   5. 对于50%占空比, 周期 = 脉宽 × 2
 *   6. 频率 = 1000000 / 周期 (Hz)
 *
 *   定时器溢出处理: 使用中断计数溢出次数, 扩展测量范围
 *   实际脉宽 = (溢出次数 × 65536 + CCR差值) us
 *
 *  【频率计算】
 *   周期_us = 捕获到的高电平脉宽 (微秒)
 *   频率_mHz = 1000000000 / (周期_us × 2)  [毫赫兹, 保留3位小数精度]
 *   其中 ×2 是因为脉宽 = 半个周期 (50%占空比)
 *
 *  【RPM 计算公式】
 *   RPM = (频率_Hz × 60) / (分频系数 × 每转脉冲数)
 *
 *   参数说明:
 *     频率_Hz        — FG_GetFrequency_mHz() 返回值 / 1000
 *     分频系数 (div)  — 信号分频比, 如传感器每N个脉冲输出1个, 则 div=N
 *                        一般为1 (不分频), 可用于减速比补偿
 *     每转脉冲数 (PPR) — 编码器/传感器每转一圈产生的脉冲数
 *                        光电传感器通常为1 (每转1个反光贴)
 *                        磁编码器可能为多个 (如每转4/8/16脉冲)
 *
 *   示例: 频率=500Hz, div=1, PPR=1
 *     RPM = 500 × 60 / (1 × 1) = 30000 RPM
 *
 *   示例: 频率=100Hz, div=2, PPR=4
 *     RPM = 100 × 60 / (2 × 4) = 750 RPM
 *
 *  【无信号处理】
 *   当周期_us = 0 时, 表示无信号输入或频率过低,
 *   FG_GetFrequency_mHz() 返回 0, FG_CalculateRPM() 返回 0
 *
 * ============================================================================
 */
#ifndef FG_CAPTURE_H
#define FG_CAPTURE_H

#include "sys.h"

// 初始化 TIM2 输入捕获
// 功能:
//   - 配置 TIM2 CH1 (PA0) 为输入捕获模式
//   - 设置预分频为 71 (72MHz / 72 = 1MHz 时基, 1us 分辨率)
//   - 开启捕获中断和更新中断 (溢出计数)
//   - 初始状态为上升沿触发
// 调用时机: 系统启动时, 在其他定时器初始化之后
void FG_Init(void);

// 获取当前测量的频率 (单位: 毫赫兹 mHz)
// 返回值:
//   - 频率值, 单位 mHz (例如返回 500000 表示 500Hz)
//   - 返回 0 表示无信号输入或频率低于测量下限
// 精度: 1mHz (千分之一赫兹), 通过脉宽微秒值反算获得
// 注意: 返回值类型 u32 最大表示约 4.29GHz, 远超测量范围
u32 FG_GetFrequency_mHz(void);

// 根据频率和传感器参数计算 RPM (转/分钟)
// 参数:
//   div            — 分频系数 (1=不分频, 2=二分频, ...)
//                     用于补偿减速箱传动比或传感器分频
//   pulses_per_rev — 每转脉冲数 (PPR), 即传感器每转一圈输出的脉冲数
//                     光电传感器: 通常为 1
//                     霍尔传感器: 通常为 1~N (取决于磁铁数量)
//                     编码器: 4/8/16/... (取决于线数和倍频方式)
// 返回值:
//   - RPM 值 (转/分钟)
//   - 返回 0 表示无信号
// 计算公式: RPM = FG_GetFrequency_mHz() × 60 / (div × pulses_per_rev × 1000)
//   即: RPM = 频率(Hz) × 60 / (分频 × PPR)
u16 FG_CalculateRPM(u8 div, u16 pulses_per_rev);

#endif
